/*Data in Sql*/

In Sql, data is the information that is stored and manipulated in a database. Data in Sql
They are organized into tables, and each table consists of rows and columns that contain specific values.
The basic concepts related to data in Sql are described below.

1. **Table**: A table is a structure that stores data in SQL. Each table is made up of rows and
columns. Each row represents a unique entry or record, and each column defines a specific attribute
of those records.

2. **Columns**: The columns of a table define the attributes or fields of the data to be
store in the table. Each column has a name and a data type that specifies what type of
Information can be stored in it (e.g. text, numbers, dates, etc.).

3. **Rows**: The rows of a table represent individual data entries. Each row contains
specific values for each column. In a table, rows are known as records.

4. **Values**: Values are the actual data that is stored in a table in the cells
corresponding to the rows and columns. These values can be of different types, such as strings
text, numbers, dates, etc.

5. **Primary key**: A primary key is a column or a set of columns that clearly identify
makes each row in a table unique. Ensures that there are no duplicates in the table rows.

6. **Foreign Key**: A foreign key is a column or set of columns that establishes a
relationship between two tables. The foreign key refers to the primary key of another table and is used
  to establish referential integrity.

7. **Indices**: Indexes are structures that speed up searching and retrieving data in a table.
They are created on one or more columns to improve query performance.

8. **Integrity Constraints**: Integrity constraints are rules that apply to data
to ensure its validity and consistency. This includes unique restrictions, key restrictions
primary and foreign key constraints.

9. **Stored procedures and functions**: Stored procedures and functions are
Database objects that can receive parameters and return results based on operations
Sql. They are used to perform more complex operations on data.

10. **Views**: Views are predefined queries that display a selection of data from one or more
boards. They are used to simplify and abstract the complexity of queries.

11. **Null data**: Sql allows the use of null values (Null) to represent the absence of data in
a column. Null values indicate that a value is not known or has not been set for that column.

Data in Sql is stored in a structured way and can be queried and manipulated using statements
Sql. The database provides a framework for organizing, managing, and retrieving data in a manner
efficient and accurate. The structure of the data, the relationships between tables and the constraints are
fundamental to guarantee the integrity and consistency of data in a database.
